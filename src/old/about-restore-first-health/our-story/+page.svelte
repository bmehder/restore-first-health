  <p>Restore First Health was established in 2018, formerly known as Prime Regen. The primary focus of the practice was to care for patients with <a href="https://restorefirsthealth.com/knee-pain/">chronic knee pain</a> in the Atlanta region. The team utilized hyaluronic knee injections to patients who wanted to avoid surgery, and receive real pain relief giving people hope for regaining their lifestyle.</p>

  <p>Within a year, we broadened our focus to treating more chronic conditions adding in <a href="https://restorefirsthealth.com/wound-care/">chronic wound care</a> and in 2022 added <a href="https://restorefirsthealth.com/diabetes-metabolic-pir-program/">diabetes care</a>, called PIR.</p>

  <p>Our goal for all our patients is offering non-invasive, non-surgical approaches to their chronic conditions. And we have succeeded! Rebranded in 2022, Restore First Health has grown into a sought after in-clinic and on-the-road chronic condition team expanding into three offices in Georgia, and adding in 2023, a new office in New Jersey.</p>

  <p>The RFH Chronic Wound Care program focuses on establishing partnerships and relationships with community providers, home health, and skilled nursing homes. Our bedside approach allows patients to be treated in their homes, and brings true healing to stalled wounds.</p>

  <p>CEO, John Fish, an established business leader for over 25 years, focuses on providing patients the best options to regain their health through non-invasive approaches in medicine.</p>

  <p>The team continues to grow and expand our reach in GA and NJ with strategic plans to reach more patients across the country.</p>
